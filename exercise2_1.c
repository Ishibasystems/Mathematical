#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main(void)
{
	//(1)
	printf("(1) 3 \n");

	//(2)
	printf("(2) %d\n", 3);

	//(3)
	printf("(3) 1 + 2 + 3 + 4 + 5\n");

	//(4)
	printf("(4) %d\n", 1 + 2 + 3 + 4 + 5);

	//(5)
	printf("(5) %d %d\n", 3 * 4, 15 / 3);

	//(6)
	printf("(6) %f\n", 1.5);

	//(7)
	printf("(7) %f\n", 3.0 * (1.0 - 5.0) / 4.0);

	//(8)
	printf("(8) %d / %f = %f\n", 19, 3.0, 19.0 / 3.0);

	//(9)
	printf("(9) %d\n", 7 % 4);

	//(10)
	printf("(10) %d\n", 11 % 3);


	exit(0);
}

