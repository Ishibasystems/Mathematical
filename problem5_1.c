#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main(void)
{
	//他に必要な変数があれば追加してよい
	int i, aa[7][15] = {
		{' ', ' ', ' ', ' ', ' ', ' ', ' ', ' ', ' ', ' ', ' ', ' ', 'n', ' ', ' '},
		{' ', ' ', ' ', ' ', ' ', ' ', ' ', ' ', ' ', ' ', '.', '\'', '_', '`', '='},
		{' ', ' ', ' ', ' ', ' ', ' ', ' ', ' ', '.', 'e', '/', ' ', ' ', ' ', ' '},
		{' ', ' ', ' ', ' ', ' ', '.', '-', 'e', ' ', '(', ' ', ' ', ' ', ' ', ' '},
		{' ', ' ', ' ', '.', 'e', ' ', '.', ' ', 'e', ')', ' ', ' ', ' ', ' ', ' '},
		{',', '-', '<', '.', '-', '-', '\'', '\\', '|', '>', ' ', ' ', ' ', ' ', ' '},
		{' ', ' ', '|', '\\', ' ', ' ', ' ', ',', '|', ' ', ' ', ' ', ' ', ' ', ' '}
	};
	double x[100], y[100], min = -logf(0), max = logf(0), xstep;

	//(1)
	for (i = 0; i < 7 * 15; printf(i % 15 == 14 ? "%c\n" : "%c", aa[i / 15][i++ % 15]));
	printf("キリン\n\n");

	//(2)
	xstep = atan(1) * 8 / (100 - 1);
	
	for (i = 0; i < 100; x[i++] = atan(1) * 8 / (100 - 1) * i);

	//(3)
	for (i = 0; i < 100; y[i++] = sin(x[i]) - 0.5 * cos(x[i]));

	for (i = 0; i < 100; i++)
		if (max < y[i]) max = y[i];
		else if (min > y[i]) min = y[i];

	printf("ymin = %f, ymax = %f\n", min, max);

	for (i = 0; i < 100; i += 33)
		printf("(x%02d, y%02d) = (%f, %f)\n", i, i, x[i], y[i]);

	exit(0);
}